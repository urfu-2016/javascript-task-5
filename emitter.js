'use strict';

/**
 * Сделано задание на звездочку
 * Реализованы методы several и through
 */
getEmitter.isStar = false;
module.exports = getEmitter;

/**
 * Возвращает новый emitter
 * @returns {Object}
 */

function getEmitter() {
    var subs = [];

    return {

        /**
         * Подписаться на событие
         * @param {String} event
         * @param {Object} context
         * @param {Function} handler
         * @returns {Object}
         */
        on: function (event, context, handler) {
            console.info(event, context, handler);
            subs.push(getNewSubscription(event, context, handler));

            return this;
        },

        /**
         * Отписаться от события
         * @param {String} event
         * @param {Object} context
         * @returns {Object}
         */
        off: function (event, context) {
            console.info(event, context);
            subs = subs.filter(function (sub) {
                var contIsCont = sub.context !== context;
                var eventIsEvent = sub.event !== event;

                return contIsCont || eventIsEvent;
            });

            return this;
        },

        /**
         * Уведомить о событии
         * @param {String} event
         * @returns {Object}
         */
        emit: function (event) {
            console.info(event);
            var doHandler = function (sub) {
                if (sub.event === event) {
                    sub.handler.call(sub.context);
                }
            };
            var len = event.split('.').length;
            for (var i = 0; i < len; i++) {
                subs.forEach(doHandler);
                event = event
                    .split('.')
                    .slice(0, -1)
                    .join('.');
            }

            return this;
        },

        /**
         * Подписаться на событие с ограничением по количеству полученных уведомлений
         * @star
         * @param {String} event
         * @param {Object} context
         * @param {Function} handler
         * @param {Number} times – сколько раз получить уведомление
         */
        several: function (event, context, handler, times) {
            console.info(event, context, handler, times);
        },

        /**
         * Подписаться на событие с ограничением по частоте получения уведомлений
         * @star
         * @param {String} event
         * @param {Object} context
         * @param {Function} handler
         * @param {Number} frequency – как часто уведомлять
         */
        through: function (event, context, handler, frequency) {
            console.info(event, context, handler, frequency);
        }
    };
}

function getNewSubscription(event, context, handler) {
    return {
        event: event,
        context: context,
        handler: handler
    };
}
